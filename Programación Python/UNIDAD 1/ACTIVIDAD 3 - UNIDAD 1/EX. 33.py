#Programa un código que permita contar el número de vocales de la siguiente frase: No hay mal que dure cien años

frase=("No hay mal que dure cien años")

a=frase.count("a")
e=frase.count("e")
i=frase.count("i")
o=frase.count("o")
u=frase.count("u")

print(f"En la frase hay {a} A")
print(f"En la frase hay {e} E")
print(f"En la frase hay {i} I")
print(f"En la frase hay {o} O")
print(f"En la frase hay {u} U")